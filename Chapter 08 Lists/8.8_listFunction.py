# Built-in functions can be used to quickly look through a list 
# without needing to write a loops:

nums = [3, 41, 12, 9, 74, 15]

print(len(nums))

print(max(nums))

print(min(nums))

print(sum(nums))

print(sum(nums)/len(nums))

